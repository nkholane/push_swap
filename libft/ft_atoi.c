#include "libft.h"

int	ft_atoi(const char *str)
{
	int res;
	int sign;
	int i;

	res = 0;
	i = 0;
	sign = 1;
	if (!str[i])
		return (0);
	while (ft_isspace(str[i]))
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			sign = -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		res = (res * 10) + (str[i] - '0');
		i++;
	}
	return (res * sign);
}
