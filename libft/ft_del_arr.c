#include "libft.h"

void       ft_del_arr(char **arr)
{
    int     i;

    i = 0;
    if (arr && *arr)
    {
        while (arr[i])
        {
            free(arr[i]);
            arr[i] = NULL;
            i++;
        }
        arr = NULL;
    }
}
